     private void llenarFormulario(String codigo){
        if (!codigo.isEmpty()){
            try {
                Producto producto = _productoBL.getByCodigo(codigo);
                
                if (producto != null)
                {
                    txtCodigo.setText(producto.getCodigo());
                    txtDescripcion.setText(producto.getDescripcion());
                    txtPrecioUnitario.setText(String.valueOf(producto.getPrecioUnitario()));
                    txtCodigo.setEnabled(false);
                }else{
                    JOptionPane.showMessageDialog(this, "Producto no existe.", "Error", JOptionPane.ERROR_MESSAGE);
                    dispose();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    
