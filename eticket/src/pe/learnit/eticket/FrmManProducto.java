    private boolean esValidoParaGuardar() throws SQLException{
        
        if (txtCodigo.getText().trim().equals("")) {
            JOptionPane.showMessageDialog( this,"Llene el Campo Codigo","PELIGRO",JOptionPane.ERROR_MESSAGE );
            return false;
        }
        if (txtDescripcion.getText().trim().equals("")) {
            JOptionPane.showMessageDialog( this,"Llene el Campo Descripcion","PELIGRO",JOptionPane.ERROR_MESSAGE );
            return false;
        }
        if (txtPrecioUnitario.getText().trim().equals("")) {
            JOptionPane.showMessageDialog( this,"Llene el Campo Precio ","PELIGRO",JOptionPane.ERROR_MESSAGE );
            return false;
        }else{
            Double n =Double.valueOf(txtPrecioUnitario.getText());
            if (n<=0) {
                
            JOptionPane.showMessageDialog( this,"Precio debe ser mayor a cero","PELIGRO",JOptionPane.ERROR_MESSAGE );
            return false;
            }
        }
         if (esNuevo && _productoBL.existe(txtCodigo.getText().trim())){
            JOptionPane.showMessageDialog(this, "Código ya utilizado.", "Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
       /* 
        ProductoBL productoBL=new ProductoBL();
        if (productoBL.existe(txtCodigo.getText().trim())) {
          JOptionPane.showMessageDialog( this,"Perroro","PELIGRO",JOptionPane.ERROR_MESSAGE );
            return false;   
        }
        */
      
        return true;
    }
